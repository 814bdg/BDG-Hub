	-- apply theme as default theme is not selected
	for i,v in pairs(BDG. BDG:GetDescendants()) do
		if v:IsA("GuiObject") then
			
			if v:IsA("ImageButton") or v:IsA("ImageLabel") then
				handleThemeColoring(v, "ImageColor3");
