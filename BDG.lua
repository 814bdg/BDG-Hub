local function openTab(tabInstance)
	
	-- Closing Of All Other Tabs
	for i,v in pairs(tabs) do
		if v.Position == UDim2.new(0, 0, 0.1, 0) or v.Position == UDim2.new(0.27, 0, 0.1, 0) and v ~= tabInstance then
